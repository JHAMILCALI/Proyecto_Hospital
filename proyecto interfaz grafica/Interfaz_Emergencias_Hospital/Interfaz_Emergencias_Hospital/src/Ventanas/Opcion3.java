    private void botonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarActionPerformed
        // TODO add your handling code here:
        try {
            String HosPitalX = campoHospitalX.getText();
            PilaHospital aux1 = new PilaHospital();
            String titulos [] = {"NOMBRE","ID","URGENCIA","EDAD","CI"};
            DefaultTableModel pantalla = new DefaultTableModel(null,titulos);
            if (!HosPitalX.equals("")) {
                while (!v1.getHos().esVacia()) {                
                Hospital hos = v1.getHos().eli();
                if (hos.getNombreHos().equals(HosPitalX)) {
                    NodoPlanta R = hos.getPlanta().getP();
				while (R!=null) {
					PilaSala_Atencion aux = new PilaSala_Atencion();
					int mayor = mayorEdad(R.getSalaAtencion());
					while (!R.getSalaAtencion().esVacia()) {
						Sala_Atencion sa = R.getSalaAtencion().eli();
						NodoPaciente Npa = sa.getPaciente().getP();
						while (Npa!=null) {
							Paciente pa = Npa.getPaciente();
							if (pa.getEdad()==mayor) {
								System.out.println("\n\tEl paciente "+pa.getNombre()+" es una persona mayor del hospital "+HosPitalX);
								//pa.mostrar();
                                                                String fila[]={pa.getNombre(),pa.getId(),pa.getUrgencia(),Integer.toString(pa.getEdad()),Integer.toString(pa.getCi())};
                                                                pantalla.addRow(fila);
                                                        }
							Npa=Npa.getSig();
						}
						aux.adi(sa);
					}
					R.getSalaAtencion().vaciar(aux);
					R=R.getSig();
				}
                }
                aux1.adi(hos);
            }
            Pantalla_Mayores_Edad.setModel(pantalla);
            v1.getHos().vaciar(aux1);
            }else{
                JOptionPane.showMessageDialog(null, "Introdusca datos correctos");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "DATOS NO VALIDOS!!!");
        }
    }//GEN-LAST:event_botonMostrarActionPerformed

