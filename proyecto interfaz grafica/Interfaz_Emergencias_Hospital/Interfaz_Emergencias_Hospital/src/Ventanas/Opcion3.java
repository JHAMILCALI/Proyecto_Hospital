    private int mayorEdad(PilaSala_Atencion a) {
        PilaSala_Atencion aux = new PilaSala_Atencion();
		int mayor=0;
		while (!a.esVacia()) {
			Sala_Atencion sa = a.eli();
			NodoPaciente R = sa.getPaciente().getP();
			while (R!=null) {
				Paciente pa = R.getPaciente();
				if (pa.getEdad()>mayor) {
					mayor=pa.getEdad();
				}
				R=R.getSig();
			}
			aux.adi(sa);
		}
		a.vaciar(aux);
		return mayor;
    }
