    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String titulos [] = {"NOMBRE HOSPITAL","DIRECION","TELEFONO"};
        DefaultTableModel pantalla = new DefaultTableModel(null,titulos);
        PilaHospital aux = new PilaHospital();
        while (!Hos.esVacia()) {            
            Hospital hos = Hos.eli();
            String fila[]={hos.getNombreHos(),hos.getDireccion(),Integer.toString(hos.getTelefono())};
            pantalla.addRow(fila);
            aux.adi(hos);
        }
        Hos.vaciar(aux);
        Pantalla_Inicio.setModel(pantalla);
       
    }//GEN-LAST:event_jButton7ActionPerformed

